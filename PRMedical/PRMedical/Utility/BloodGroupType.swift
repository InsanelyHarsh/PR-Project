    case ANeg = "A Negative"
    
    case BPos = "B Positive"
    case BNeg = "B Negative"
    
    case OPos = "O Positive"
    case ONeg = "O Negative"
    
    case AbPos = "AB Positive"
    case ABNeg = "AB Negative"
}
